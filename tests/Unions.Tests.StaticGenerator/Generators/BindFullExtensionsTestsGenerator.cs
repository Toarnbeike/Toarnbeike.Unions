using Toarnbeike.Unions.Tests.Utitilies;

namespace Toarnbeike.Unions.Tests.Generators;

/// <summary>
/// Generates tests for the Bind full extension methods.
/// This code is build-time tooling; readability and determinism are preferred over abstraction.
/// </summary>
internal static class BindFullExtensionsTestsGenerator
{
    private static int[] _arities = null!;
    private static string _union = null!;

    public static string Generate(int arity)
    {
        _arities = [.. Enumerable.Range(1, arity)];
        _union = $"Union<{string.Join(',', _arities.Select(i => "U" + i))}>";

        return $$"""
                 {{AutoGeneratedMessageGenerator.GetAutoGeneratedMessage(nameof(BindFullExtensionsTestsGenerator))}}

                 using Toarnbeike.Unions.Generic;
                 using Toarnbeike.Unions.Generic.Extensions;
                 using Toarnbeike.Unions.Generic.TestExtensions;
                 using Toarnbeike.Unions.Tests.TestTypes;
                 // Resharper disable UnusedParameter.Local

                 namespace Toarnbeike.Unions.Tests.Extensions.Bind.Generated;

                 public class Bind{{arity:D2}}FullTests
                 {
                 {{string.Join("\n", _arities.Select(FactoryMethod))}}
                 {{HelperMethods()}}
                 {{string.Join("\n", _arities.Select(Bind))}}
                 {{string.Join("\n", _arities.Select(BindAsync))}}
                 {{string.Join("\n", _arities.Select(TaskBind))}}
                 {{string.Join("\n", _arities.Select(TaskBindAsync))}}
                 }
                 """;
    }

    private static string FactoryMethod(int arity)
    {
        return $"    private static {_union} MakeT{arity}(int v) => {_union}.FromT{arity}(new U{arity}(v));";
    }

    private static string HelperMethods()
    {
        return $"""

                    private static {_union} ShouldNotBeCalled(int original) => throw new ShouldNotBeCalledException();
                    private static Task<{_union}> ShouldNotBeCalledAsync(int original) => throw new ShouldNotBeCalledException();
                """;
    }

    private static string Bind(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public void Bind_Should_InvokeT{{arity}}Func_WhenT{{arity}}()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         var result = union.Bind(
                             {{string.Join(",\n            ", _arities.Select(i => BindLine(i, i == arity, otherArity)))}}
                         );
                         
                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                     }
                     
                     [Test]
                     public void Bind_Throws_WhenT{{arity}}Func_ReturnsNull()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         
                         Should.Throw<ArgumentNullException>(() => union.Bind(
                             {{string.Join(",\n            ", _arities.Select(i => BindLineNull(i, i == arity)))}}
                         ));
                     }   
                 """;
    }

    private static string BindAsync(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public async Task BindAsync_Should_InvokeT{{arity}}Func_WhenT{{arity}}()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         var result = await union.BindAsync(
                             {{string.Join(",\n            ", _arities.Select(i => BindLineAsync(i, i == arity, otherArity)))}}
                         );
                         
                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                     }
                     
                     [Test]
                     public async Task BindAsync_Throws_WhenT{{arity}}Func_ReturnsNull()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         
                         await Should.ThrowAsync<ArgumentNullException>(() => union.BindAsync(
                             {{string.Join(",\n            ", _arities.Select(i => BindLineNullAsync(i, i == arity)))}}
                         ));
                     }   
                 """;
    }


    private static string TaskBind(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public async Task TaskBind_Should_InvokeT{{arity}}Func()
                     {
                         var task = Task.FromResult(MakeT{{arity}}({{arity}}));
                         
                         var result = await task.Bind(
                             {{string.Join(",\n            ", _arities.Select(i => BindLine(i, i == arity, otherArity)))}}
                         );
                         
                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                     }
                 """;
    }

    private static string TaskBindAsync(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public async Task TaskBindAsync_Should_InvokeT{{arity}}Func()
                     {
                         var task = Task.FromResult(MakeT{{arity}}({{arity}}));

                         var result = await task.BindAsync(
                             {{string.Join(",\n            ", _arities.Select(i => BindLineAsync(i, i == arity, otherArity)))}}
                         );
                         
                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                     }
                 """;
    }

    private static string BindLine(int arity, bool isTarget, int otherArity)
    {
        return isTarget
            ? $"u{arity} => MakeT{otherArity}(u{arity}.Value + 10)"
            : $"u{arity} => ShouldNotBeCalled(u{arity}.Value)";
    }

    private static string BindLineNull(int arity, bool isTarget)
    {
        return isTarget
            ? "null!"
            : $"u{arity} => ShouldNotBeCalled(u{arity}.Value)";
    }

    private static string BindLineAsync(int arity, bool isTarget, int otherArity)
    {
        return isTarget
            ? $"u{arity} => Task.FromResult(MakeT{otherArity}(u{arity}.Value + 10))"
            : $"u{arity} => ShouldNotBeCalledAsync(u{arity}.Value)";
    }

    private static string BindLineNullAsync(int arity, bool isTarget)
    {
        return isTarget
            ? "null!"
            : $"u{arity} => ShouldNotBeCalledAsync(u{arity}.Value)";
    }
}
