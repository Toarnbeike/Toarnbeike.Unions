using Toarnbeike.Unions.Utitilies;

namespace Toarnbeike.Unions.Generators;

/// <summary>
/// Generates tests for the MapT extension methods.
/// This code is build-time tooling; readability and determinism are preferred over abstraction.
/// </summary>
internal static class MapPartialExtensionsTestsGenerator
{
    private static int[] _arities = null!;
    private static string _union = null!;

    public static string Generate(int arity)
    {
        _arities = [.. Enumerable.Range(1, arity)];
        _union = $"Union<{string.Join(',', _arities.Select(i => "U" + i))}>";

        return $$"""
                 {{AutoGeneratedMessageGenerator.GetAutoGeneratedMessage(nameof(MapPartialExtensionsTestsGenerator))}}

                 using Toarnbeike.Unions.Extensions;
                 using Toarnbeike.Unions.TestExtensions;
                 using Toarnbeike.Unions.Tests.Unions.TestTypes;
                 // Resharper disable UnusedParameter.Local

                 namespace Toarnbeike.Unions.Tests.Extensions.Map.Generated;

                 public class Map{{arity:D2}}PartialTests
                 {
                 {{string.Join("\n", _arities.Select(FactoryMethod))}}
                 {{HelperMethods()}}
                 {{string.Join("\n", _arities.Select(MapT))}}
                 {{string.Join("\n", _arities.Select(MapTAsync))}}
                 {{string.Join("\n", _arities.Select(TaskMapT))}}
                 {{string.Join("\n", _arities.Select(TaskMapTAsync))}}
                 }
                 """;
    }

    private static string FactoryMethod(int arity)
    {
        return $"    private static {_union} MakeT{arity}(int v) => {_union}.FromT{arity}(new U{arity}(v));";
    }

    private static string HelperMethods()
    {
        return $"""

                    private static T ShouldNotBeCalled<T>(T original) => throw new ShouldNotBeCalledException();
                    private static Task<T> ShouldNotBeCalledAsync<T>(T original) => throw new ShouldNotBeCalledException();
                """;
    }

    private static string MapT(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public void MapT{{arity}}_Should_Invoke_T{{arity}}_Mapper()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         var result = union.MapT{{arity}}(u{{arity}} => new U{{arity}}(u{{arity}}.Value + 10));
                         
                         result.ShouldBeT{{arity}}().Value.ShouldBe({{arity + 10}});
                     }

                     [Test]
                     public void MapT{{arity}}_Should_RemainUnmodified_WhenOtherArity()
                     {
                         var union = MakeT{{otherArity}}({{otherArity}});
                         var result = union.MapT{{arity}}(u{{arity}} => ShouldNotBeCalled(u{{arity}}));

                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{otherArity}});
                     }

                     [Test]
                     public void MapT{{arity}}_Throws_When_T{{arity}}_Mapper_ReturnsNull()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         
                         Should.Throw<ArgumentNullException>(() => union.MapT{{arity}}(null!));
                     }
                 """;
    }

    private static string MapTAsync(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;

        return $$"""

                     [Test]
                     public async Task MapT{{arity}}Async_Should_Invoke_T{{arity}}_Mapper()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         var result = await union.MapT{{arity}}Async(u{{arity}} => Task.FromResult(new U{{arity}}(u{{arity}}.Value + 10)));
                         
                         result.ShouldBeT{{arity}}().Value.ShouldBe({{arity + 10}});
                     }

                     [Test]
                     public async Task MapT{{arity}}Async_Should_RemainUnmodified_WhenOtherArity()
                     {
                         var union = MakeT{{otherArity}}({{otherArity}});
                         var result = await union.MapT{{arity}}Async(u{{arity}} => ShouldNotBeCalledAsync(u{{arity}}));
                 
                         result.ShouldBeT{{otherArity}}().Value.ShouldBe({{otherArity}});
                     }

                     [Test]
                     public async Task MapT{{arity}}Async_Throws_When_T{{arity}}_Mapper_ReturnsNull()
                     {
                         var union = MakeT{{arity}}({{arity}});
                         
                         await Should.ThrowAsync<ArgumentNullException>(() => union.MapT{{arity}}Async(null!));
                     }
                 """;
    }

    private static string TaskMapT(int arity)
    {
        return $$"""

                     [Test]
                     public async Task TaskMapT{{arity}}_Should_Invoke_T{{arity}}_Mapper()
                     {
                         var task = Task.FromResult(MakeT{{arity}}({{arity}}));

                         var result = await task.MapT{{arity}}(u{{arity}} => new U{{arity}}(u{{arity}}.Value + 10));
                         
                         result.ShouldBeT{{arity}}().Value.ShouldBe({{arity + 10}});
                     }
                 """;
    }

    private static string TaskMapTAsync(int arity)
    {

        return $$"""

                     [Test]
                     public async Task TaskMapT{{arity}}Async_Should_Invoke_T{{arity}}_Mapper()
                     {
                         var task = Task.FromResult(MakeT{{arity}}({{arity}}));

                         var result = await task.MapT{{arity}}Async(u{{arity}} => Task.FromResult(new U{{arity}}(u{{arity}}.Value + 10)));
                         
                         result.ShouldBeT{{arity}}().Value.ShouldBe({{arity + 10}});
                     }
                 """;
    }
}
