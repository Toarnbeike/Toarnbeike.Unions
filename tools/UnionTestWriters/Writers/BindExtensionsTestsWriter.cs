using WriterLibrary;

namespace UnionTestWriters.Writers;

/// <summary>
/// Generates tests for the Bind partial extension methods.
/// This code is build-time tooling; readability and determinism are preferred over abstraction.
/// </summary>
internal static class BindExtensionsTestsWriter
{
    private static int[] _arities = null!;
    private static string _union = null!;

    public static string WriteFile(int arity)
    {
        _arities = [.. Enumerable.Range(1, arity)];
        _union = $"Union<{string.Join(',', _arities.Select(i => "U" + i))}>";

        return $$"""
                 {{AutoGeneratedMessageWriter.GetAutoGeneratedMessage(typeof(BindExtensionsTestsWriter).FullName!)}}

                 using Toarnbeike.Unions.Generic;
                 using Toarnbeike.Unions.Generic.Extensions;
                 using Toarnbeike.Unions.Generic.TestExtensions;
                 using Toarnbeike.Unions.Tests.TestTypes;

                 namespace Toarnbeike.Unions.Tests.Extensions.Bind;

                 public class Bind{{arity:D2}}Tests
                 {
                 {{_arities.Select(FactoryMethod).JoinLines().Indent(1)}}
                 
                 {{HelperMethods().Indent(1)}}
                 
                 {{_arities.Select(BindT).JoinParagraphs().Indent(1)}}
                 
                 {{_arities.Select(BindTAsync).JoinParagraphs().Indent(1)}}
                 
                 {{_arities.Select(TaskBindT).JoinParagraphs().Indent(1)}}
                 
                 {{_arities.Select(TaskBindTAsync).JoinParagraphs().Indent(1)}}
                 }
                 """;
    }
    private static string FactoryMethod(int arity)
    {
        return $"private static {_union} MakeT{arity}(int v) => {_union}.FromT{arity}(new U{arity}(v));";
    }

    private static string HelperMethods()
    {
        return $"""
                private static {_union} ShouldNotBeCalled(int _) => throw new ShouldNotBeCalledException();
                private static Task<{_union}> ShouldNotBeCalledAsync(int _) => throw new ShouldNotBeCalledException();
                """;
    }

    private static string BindT(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;
        return $$"""
                 [Test]
                 public void BindT{{arity}}_Should_Invoke_T{{arity}}Func_WhenT{{arity}}()
                 {
                     var union = MakeT{{arity}}({{arity}});
                     var result = union.BindT{{arity}}(u{{arity}} => MakeT{{otherArity}}(u{{arity}}.Value + 10));
                     
                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                 }

                 [Test]
                 public void BindT{{arity}}_Should_NotInvoke_WhenT{{otherArity}}()
                 {
                     var union = MakeT{{otherArity}}({{otherArity}});
                     var result = union.BindT{{arity}}(u{{arity}} => ShouldNotBeCalled(u{{arity}}.Value));

                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{otherArity}});
                 }

                 [Test]
                 public void BindT{{arity}}_Throws_When_T{{arity}}Func_ReturnsNull()
                 {
                     var union = MakeT{{arity}}({{arity}});
                     
                     Should.Throw<ArgumentNullException>(() => union.BindT{{arity}}(null!));
                 }
                 """;
    }

    private static string BindTAsync(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;
        return $$"""
                 [Test]
                 public async Task BindT{{arity}}Async_Should_Invoke_T{{arity}}Func_WhenT{{arity}}()
                 {
                     var union = MakeT{{arity}}({{arity}});
                     var result = await union.BindT{{arity}}Async(u{{arity}} => Task.FromResult(MakeT{{otherArity}}(u{{arity}}.Value + 10)));
                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                 }

                 [Test]
                 public async Task BindT{{arity}}Async_Should_NotInvoke_WhenT{{otherArity}}()
                 {
                     var union = MakeT{{otherArity}}({{otherArity}});
                     var result = await union.BindT{{arity}}Async(u{{arity}} => ShouldNotBeCalledAsync(u{{arity}}.Value));
                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{otherArity}});
                 }

                 [Test]
                 public async Task BindT{{arity}}Async_Throws_When_T{{arity}}Func_ReturnsNull()
                 {
                     var union = MakeT{{arity}}({{arity}});
                     
                     await Should.ThrowAsync<ArgumentNullException>(() => union.BindT{{arity}}Async(null!));
                 }
                 """;
    }

    private static string TaskBindT(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;
        return $$"""
                 [Test]
                 public async Task TaskBindT{{arity}}_Should_Invoke_T{{arity}}Func()
                 {
                     var task = Task.FromResult(MakeT{{arity}}({{arity}}));

                     var result = await task.BindT{{arity}}(u{{arity}} => MakeT{{otherArity}}(u{{arity}}.Value + 10));
                     
                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                 }
                 """;
    }


    private static string TaskBindTAsync(int arity)
    {
        var otherArity = arity == 1 ? 2 : 1;
        return $$"""
                 [Test]
                 public async Task TaskBindT{{arity}}Async_Should_Invoke_T{{arity}}Func()
                 {
                     var task = Task.FromResult(MakeT{{arity}}({{arity}}));

                     var result = await task.BindT{{arity}}Async(u{{arity}} => Task.FromResult(MakeT{{otherArity}}(u{{arity}}.Value + 10)));
                     
                     result.ShouldBeT{{otherArity}}().Value.ShouldBe({{arity + 10}});
                 }
                 """;
    }
}