using Toarnbeike.Unions.Utilities;

namespace Toarnbeike.Unions.Generators;

/// <summary>
/// Generates the Union.Switch extension methods for the requested arities.
/// This code is build-time tooling; readability and determinism are preferred over abstraction.
/// </summary>
internal static class SwitchExtensionsGenerator
{
    public static string Generate(IEnumerable<int> arities)
    {
        return $$"""
            {{AutoGeneratedMessageGenerator.GetAutoGeneratedMessage(nameof(SwitchExtensionsGenerator))}}

            namespace Toarnbeike.Unions.Extensions;

            /// <summary>
            /// Switch: extension methods to perform an action on union values.
            /// </summary>
            public static class SwitchExtensions
            {{{string.Join("\n", arities.Select(GenerateSwitch))}}
            }
            """;
    }

    private static string GenerateSwitch(int arity)
    {
        var collection = Enumerable.Range(1, arity).ToList();
        var genericArgs = $"<{string.Join(", ", collection.Select(i => $"T{i}"))}>";

        return $$"""

                     #region Switch Extensions for Union{{genericArgs}}

                     extension{{genericArgs}}(Union{{genericArgs}} union)
                     {
                         /// <summary>
                         /// Switch on the value stored in the union and invokes the corresponding side effect based on its type.
                         /// </summary>
                         {{string.Join("\n        ", collection.Select(i => ParamXmlComments(i, false)))}}
                         public void Switch({{string.Join(", ", collection.Select(i => $"Action<T{i}> onT{i}"))}})
                         {
                             {{string.Join("\n            ", collection.Select(i => $"ArgumentNullException.ThrowIfNull(onT{i});"))}}
                 
                             switch (union.Index)
                             {
                                 {{string.Join("\n                ", collection.Select(i => SwitchLine(i, false)))}}
                                 default: throw new InvalidOperationException($"Union index {union.Index} is invalid for Union{{genericArgs}}.");
                             };
                         }
                         
                         /// <summary>
                         /// Switch on the value stored in the union and invokes the corresponding async side effect based on its type.
                         /// </summary>
                         {{string.Join("\n        ", collection.Select(i => ParamXmlComments(i, true)))}}
                         public async Task SwitchAsync({{string.Join(", ", collection.Select(i => $"Func<T{i}, Task> onT{i}Async"))}})
                         {
                             {{string.Join("\n            ", collection.Select(i => $"ArgumentNullException.ThrowIfNull(onT{i}Async);"))}}
                 
                             switch (union.Index)
                             {
                                 {{string.Join("\n                ", collection.Select(i => SwitchLine(i, true)))}}
                                 default: throw new InvalidOperationException($"Union index {union.Index} is invalid for Union{{genericArgs}}.");
                             };
                         }
                     }
                     
                     extension{{genericArgs}}(Task<Union{{genericArgs}}> unionTask)
                     {
                         /// <summary>
                         /// Switch on the value stored in the union and invokes the corresponding side effect based on its type.
                         /// </summary>
                         {{string.Join("\n        ", collection.Select(i => ParamXmlComments(i, false)))}}
                         public async Task Switch({{string.Join(", ", collection.Select(i => $"Action<T{i}> onT{i}"))}})
                         {
                             var union = await unionTask.ConfigureAwait(false);
                             union.Switch({{string.Join(", ", collection.Select(i => $"onT{i}"))}});
                         }
                         
                         /// <summary>
                         /// Switch on the value stored in the union and invokes the corresponding async side effect based on its type.
                         /// </summary>
                         {{string.Join("\n        ", collection.Select(i => ParamXmlComments(i, true)))}}
                         public async Task SwitchAsync({{string.Join(", ", collection.Select(i => $"Func<T{i}, Task> onT{i}Async"))}})
                         {
                             var union = await unionTask.ConfigureAwait(false);
                             await union.SwitchAsync({{string.Join(", ", collection.Select(i => $"onT{i}Async"))}});
                         }
                     }

                     #endregion
                 """;
    }

    private static string ParamXmlComments(int arity, bool async)
    {
        var asyncSuffix = async ? "Async" : "";
        var asyncComment = async ? "async " : "";
        return
            $"/// <param name=\"onT{arity}{asyncSuffix}\">The {asyncComment}action to invoke when the union holds a value of type T{arity}.</param>";
    }

    private static string SwitchLine(int arity, bool async)
    {
        return async ?
            $"case {arity}: await onT{arity}Async(union.AsT{arity}).ConfigureAwait(false); break;" :
            $"case {arity}: onT{arity}(union.AsT{arity}); break;";
    }
}