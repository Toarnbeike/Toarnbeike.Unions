using Toarnbeike.SourceGeneration.Models.Rendering;
using Toarnbeike.SourceGeneration.Rendering;
using Toarnbeike.Unions.SourceGenerator.Emitters.Rendering;
using Toarnbeike.Unions.SourceGenerator.Models;

namespace Toarnbeike.Unions.SourceGenerator.Emitters;

internal static class UnionEmitter
{
    public static string Emit(UnionModel model)
    {
        return $$"""
                {{AutoGeneratedMessage.Create(model.Name, nameof(UnionEmitter))}}
                
                using System.Diagnostics;
                using System.Diagnostics.CodeAnalysis;
                using Toarnbeike.Unions.Generic;
                {{model.CaseUsings}}

                namespace {{model.Namespace}};

                partial class {{model.Name}}
                {
                    private readonly {{model.BackingUnionType}} {{model.BackingFieldName}};

                    private {{model.Name}}({{model.BackingUnionType}} value)
                    {
                        {{model.BackingFieldName}} = value;
                    }

                {{model.Cases.Select(caseModel => GenerateConstructor(model, caseModel)).JoinParagraphs().Indent(1)}}
                {{model.Cases.Select(GenerateIsProperty).JoinParagraphs().Indent(1)}}
                {{model.Cases.Select(GenerateTryGetMethod).JoinParagraphs().Indent(1)}}
                    internal {{model.BackingUnionType}} AsUnion() => {{model.BackingFieldName}};
                
                    internal static {{model.Name}} FromUnion({{model.BackingUnionType}} union) => new(union);

                    internal static async Task<{{model.Name}}> FromUnionAsync(Task<{{model.BackingUnionType}}> unionTask) => new(await unionTask);

                    public static implicit operator {{model.Name}}({{model.BackingUnionType}} union) => FromUnion(union);
                }
                """;
    }


    private static string GenerateConstructor(UnionModel model, UnionCaseModel caseModel)
    {
        return $"""
                 /// <summary>
                 /// Creates a {model.Name} in the {caseModel.Name} state.
                 /// </summary>
                 {caseModel.ConstructorParameters.GetXmlComments()}
                 public static {model.Name} {caseModel.Name}({caseModel.ConstructorParameters.GetAsParameters()}) =>
                     new({model.BackingUnionType}.FromT{caseModel.Index}(new {caseModel.Name}({caseModel.ConstructorParameters.GetAsArguments()})));

                 /// <summary>
                 /// Creates a new union instance by providing the {caseModel.Name} to store in it.
                 /// </summary>
                 /// <param name="value">The {caseModel.Name} instance to store in the union.</param>
                 public static {model.Name} {caseModel.Name}({caseModel.TypeName} value)
                     => new({model.BackingUnionType}.FromT{caseModel.Index}(value));
                 """;
    }

    private static string GenerateIsProperty(UnionCaseModel caseModel)
    {
        return $"""
                /// <summary>
                /// Indicates whether the union is in {caseModel.Name} state.
                /// </summary>
                public bool Is{caseModel.Name} => _value.IsT{caseModel.Index};
                """;
    }

    private static string GenerateTryGetMethod(UnionCaseModel caseModel)
    {
        return $"""
                /// <summary>
                /// Attempts to get the stored {caseModel.Name} value from the union.
                /// </summary>
                /// <param name="value">When this method returns, contains the {caseModel.Name} value if the union is in that state; otherwise, the default value for {caseModel.TypeName}.</param>
                /// <returns>true if the union is in {caseModel.Name} state; otherwise, false.</returns>
                public bool TryGet{caseModel.Name}([MaybeNullWhen(false)] out {caseModel.TypeName} value) => 
                   _value.TryGetT{caseModel.Index}(out value);
                """;
    }
}
